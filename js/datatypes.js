{
	// This is primitive datatype
	let userName = "Alex Smith";
	console.log(userName.toUpperCase());
	console.log(typeof(userName));

	let age = 35;
	console.log(typeof(age));
	// console.log(age);
	

	// This is objects
	let userName1 = new String("John Smith");
	console.log(typeof(userName1));
	console.log(userName1.valueOf());
	console.log(userName1.toUpperCase());

	let age1 = new Number(45);
	console.log(typeof(age1));
	console.log(age1.valueOf());
}